package entity;

import java.io.Serializable;

/**
 *
 * @author pupil
 */
public class Shop implements Serializable{
    int money;
    
    public Shop(){
    }

    public int getMoney() {
        return money;
    }

    public void setMoney(int money) {
        this.money = money;
    }

    @Override
    public String toString() {
        return "Shop money: " + money/100;
    }
    
    
}
